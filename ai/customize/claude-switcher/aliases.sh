# Claude Code Mode Switcher Aliases
# Add these to your ~/.bashrc or ~/.zshrc
# Automatically configured by installer

# Main command - Interactive menu
alias cc-change='~/.local/bin/cc-change'

# Quick mode switching (shortcuts)
alias cc-native='cc-change cc-native'
alias cc-glm='cc-change cc-glm'
alias cc-mixed='cc-change cc-mixed'
alias fast-glm='cc-change fast-glm'

# Information commands
alias cc-status='cc-change --status'
alias cc-list='cc-change --list'
alias cc-help='cc-change --help'
